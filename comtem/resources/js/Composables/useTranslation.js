export function useTranslation() {
    const t = (key) => messages[locale.value]?.[key] ?? messages.en[key] ?? key;

    const toggleLocale = () => {
        locale.value = locale.value === 'en' ? 'lv' : 'en';
        localStorage.setItem('locale', locale.value);
    };

    return { locale, t, toggleLocale };
}
